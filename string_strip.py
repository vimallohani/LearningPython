name='       Harshit        '
dots='--------------'
print(name+dots)

#lstrip method is used for leading spaces
print(name.lstrip()+dots)

#rstrip method is used for leading spaces
print(name.rstrip()+dots)

#strip method is used to remove leading and trailing spaces
print(name.strip()+dots)

#to remove all spaces from string use replace method
a='     vimal lohani   '
print(a.replace(" ",""))
